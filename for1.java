public class for1 { 
        
        public static void main(String[] args) {
            // Usamos un bucle for para repetir 5 veces
            for (int i = 1; i <= 10; i++) {
                System.out.println("Iteración número: " + i); // Imprimimos el número de la iteración actual
            }
    
            System.out.println("¡Bucle terminado!");
        }
    
}
